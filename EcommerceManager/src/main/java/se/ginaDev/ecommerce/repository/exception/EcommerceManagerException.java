package se.ginaDev.ecommerce.repository.exception;

public class EcommerceManagerException extends Exception{

	private static final long serialVersionUID = 1930471071396525363L;

	public EcommerceManagerException() {
		super();
	}

	public EcommerceManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public EcommerceManagerException(String message) {
		super(message);
	}

	public EcommerceManagerException(Throwable cause) {
		super(cause);
	}
	
	
}
